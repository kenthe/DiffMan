%    array         - Create arrays of objects.
%    dmargcheck    - Turn on/off input checking in DiffMan.
%    dmhelp        - The help facility in DiffMan.
%    dmprogrep     - Turn on/off progress report.
%    iseven        - Function to test if a number is even.
%    iscellempty   - Check if a cell tree consists of empty matrices.
%    iseven        - Check if integer is even.
%    repprogress   - The progress report m-file.
%    dmisinteger   - Function to test if a number is integer.
%    skew          - Creates a skew matrix from a 3-vector.
%
% .. and the following utility directories:
%
%    flowtools     - Functions for effiency and order checks.
%    lafreeutil    - Utility functions for the free Lie algebra.

